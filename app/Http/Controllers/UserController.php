     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function AddFavorite($id, Request $request)
    {
        $user=User::find($request->user);
        $favorites= $user->favorite_id;
        if (in_array((int)$id, $favorites)) {
            return response()->json(['message'=>'Already Picked!']);
        }
        array_push ( $favorites, (int)$id);
        $user->update(['favorite_id'=> $favorites]);
        return response()->json(['message'=>'Successfully Saved!', 'data' =>$user ]);
    }

